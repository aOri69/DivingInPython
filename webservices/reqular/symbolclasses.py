import re
"""
[abcd1234] = [a-d1-4] = один символ из множества
\d = [0123456789] = [0-9]
\D = [^\d] = [^0-9]
{1,2} = от 1 до 2 раз
* = {0,беск}
+ = {1,беск}
? = {0,1}
"""
if __name__ == '__main__':
    text = """
    Автомобиль с номером А123ВС77 подрезал автомобиль
    K654HE197, спровоцировав ДТП с участием еще двух автомобилей
    с номерами M542OP777 и O007OO77
    """
    pattern = r'[ABEKMHOPCTYX]\d{3}[ABEKMHOPCTYX]\d{2}\d{2,3}'
    print(re.findall(pattern, text))
